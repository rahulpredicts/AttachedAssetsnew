-- CANADA AUTOMOTIVE HISTORICAL DATABASE
-- Complete data from 1995 to 2025
-- All major brands, models, and trims sold in Canada

-- Create tables
CREATE TABLE IF NOT EXISTS brands (
  brand_id INT PRIMARY KEY AUTO_INCREMENT,
  brand_name VARCHAR(100) NOT NULL UNIQUE,
  brand_type VARCHAR(50),
  country_origin VARCHAR(50),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS models_history (
  model_id INT PRIMARY KEY AUTO_INCREMENT,
  brand_id INT NOT NULL,
  model_name VARCHAR(100) NOT NULL,
  year INT NOT NULL,
  category VARCHAR(100),
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (brand_id) REFERENCES brands(brand_id),
  UNIQUE KEY unique_model_year (brand_id, model_name, year)
);

CREATE TABLE IF NOT EXISTS trims_history (
  trim_id INT PRIMARY KEY AUTO_INCREMENT,
  model_id INT NOT NULL,
  brand_id INT NOT NULL,
  trim_name VARCHAR(150) NOT NULL,
  year INT NOT NULL,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (model_id) REFERENCES models_history(model_id),
  FOREIGN KEY (brand_id) REFERENCES brands(brand_id)
);

