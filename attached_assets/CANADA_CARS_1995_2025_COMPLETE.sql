-- Summary Statistics Query
SELECT 
  COUNT(DISTINCT b.brand_name) as total_brands,
  COUNT(DISTINCT m.model_name) as unique_models,
  COUNT(m.model_id) as total_model_year_combos,
  MIN(m.year) as earliest_year,
  MAX(m.year) as latest_year
FROM brands b
LEFT JOIN models_history m ON b.brand_id = m.brand_id;

-- Brand breakdown
SELECT b.brand_name, COUNT(DISTINCT m.model_name) as models_count, COUNT(m.model_id) as records
FROM brands b
LEFT JOIN models_history m ON b.brand_id = m.brand_id
GROUP BY b.brand_id, b.brand_name
ORDER BY records DESC;
